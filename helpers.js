import h from "../snabbdom/h"

const dom = tag => (...args) => {
  // selector argument is optional, so first argument could be either be the selector or something else
  if (typeof args[0] === 'string') {
    args[0] = tag + args[0]
  }
  else {
    args.unshift(tag)
  }
  return h(...args)
}
